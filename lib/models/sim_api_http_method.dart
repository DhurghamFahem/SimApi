/// Enum representing HTTP methods.
enum SimApiHttpMethod {
  /// Represents the HTTP POST method used to create a new resource.
  post,

  /// Represents the HTTP GET method used to retrieve data.
  get,

  /// Represents the HTTP PUT method used to update a resource.
  put,

  /// Represents the HTTP PATCH method used to partially update a resource.
  patch,

  /// Represents the HTTP DELETE method used to remove a resource.
  delete,
}
